#ifndef _wlu_otp_h_
#define _wlu_otp_h_

extern void wluc_otp_module_init(void);

typedef struct otpcrc_config {
	uint16 end_addr;	/* End address of CRC block. start_addr = end_addr-num_crc+1 */
	uint8 num_crc;	/* Number of CRC to store */
	uint8 crc_ver;	/* CRC version. For future expension */

} otpcrc_config_t;

/* CRC configuration validation return values. */
#define BCME_VALID_CRCCONFIG		0
#define BCME_NO_CRCCONFIG		-1
#define BCME_BAD_CRCCONFIG		-2

#endif /* _wlu_otp_h_ */
