void
phy_ac_update_phycorestate(phy_info_t *pi)
{
	uint16 phymode = phy_get_phymode(pi);

	PHY_TRACE(("wl%d: %s\n", pi->sh->unit, __FUNCTION__));
	if (ACMAJORREV_33(pi->pubpi->phy_rev) && phymode != PHYMODE_BGDFS) {
		pi->pubpi->phy_corenum = READ_PHYREGFLD(pi, PhyCapability0, NumberOfAntennas);
	} else {
		pi->pubpi->phy_corenum = READ_PHYREGFLD(pi, PhyCapability0, NumberOfStreams);
	}

	pi->pubpi->phy_corenum >>= (((phymode == PHYMODE_MIMO) ||
		(phymode == PHYMODE_80P80)) ? 0 : 1);

	if ((phymode == PHYMODE_RSDB) && (phy_get_current_core(pi) == PHY_RSBD_PI_IDX_CORE1)) {
		phy_info_t *other_pi = phy_get_other_pi(pi);
		pi->pubpi->phy_corenum = other_pi->pubpi->phy_corenum;
	}

	pi->pubpi->phy_coremask = ((1 << pi->pubpi->phy_corenum) - 1);

	if (pi->tempi)
		phy_ac_update_tempsense_bitmap(pi);

	PHY_TRACE(("wl%d phy_corenum:%d phy_coremask:%d\n",
		pi->sh->unit, pi->pubpi->phy_corenum, pi->pubpi->phy_coremask));
}

void phy_regaccess_war_acphy(phy_info_t *pi)
{
	uint16 phymode = phy_get_phymode(pi);
	d11regs_t *regs;
	uint8 curr_core, other_core;
	uint32 reg_val = 0, mac_ctl = 0;
	uint32 desired_bit_val = 0, inv_bit_mask;

	ASSERT(ACMAJORREV_4(pi->pubpi->phy_rev));
	BCM_REFERENCE(regs);
	curr_core = phy_get_current_core(pi);
	other_core = 1 - curr_core;

	if ((phymode == PHYMODE_MIMO) || (phymode == PHYMODE_80P80))
		desired_bit_val = (1 << MAC_IHRP_CLOCK_EN);
	else
		desired_bit_val = (0 << MAC_IHRP_CLOCK_EN);

	inv_bit_mask = ~ (1 << MAC_IHRP_CLOCK_EN);

	if (BUSTYPE(pi->sh->sih->bustype) == SI_BUS) {
		/* First set MACPhyPhyIHRPClockEn bit in PHY_CTL (bit 15) of curr_core */
		reg_val = R_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi));
		W_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi),
			((reg_val & inv_bit_mask) | desired_bit_val));

		reg_val = R_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi));

		/* setup registers to enable accessing other core registers */
		regs = (d11regs_t *)wlapi_si_d11_switch_addrbase(pi->sh->physhim, other_core);
		mac_ctl = R_REG(pi->sh->osh, D11_MACCONTROL(pi));
		W_REG(pi->sh->osh, D11_MACCONTROL(pi), (mac_ctl| MCTL_IHR_EN));

		/* check MACPhyPhyIHRPClockEn bit in PHY_CTL (bit 15) of other core */
		reg_val = R_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi));
		if ((reg_val & ~inv_bit_mask) != desired_bit_val) {
			W_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi),
				((reg_val & inv_bit_mask) | desired_bit_val));
		}

		/* Now restore maccontrol and core */
		W_REG(pi->sh->osh, D11_MACCONTROL(pi), mac_ctl);
		wlapi_si_d11_switch_addrbase(pi->sh->physhim, curr_core);
	} else {
		uint32 curr_coreidx, other_coreidx;
		curr_coreidx = si_findcoreidx(pi->sh->sih, D11_CORE_ID, curr_core);
		other_coreidx = si_findcoreidx(pi->sh->sih, D11_CORE_ID, other_core);

		/* First set MACPhyPhyIHRPClockEn bit in PHY_CTL (bit 15) of curr_core */
		reg_val = R_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi));

		W_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi),
			((reg_val & inv_bit_mask) | desired_bit_val));

		reg_val = R_REG(pi->sh->osh, D11_PSM_PHY_CTL(pi));

		/* setup registers to enable accessing other core registers */
		regs = wlapi_si_d11_switch_addrbase(pi->sh->physhim, other_coreidx);
		mac_ctl = R_REG(pi->sh->osh, D11_MACCONTROL_ALTBASE(regs, pi->regoffsets));

		W_REG(pi->sh->osh, D11_MACCONTROL_ALTBASE(regs, pi->regoffsets),
			(mac_ctl| MCTL_IHR_EN));

		/* check MACPhyPhyIHRPClockEn bit in PHY_CTL (bit 15) of other core */
		reg_val = R_REG(pi->sh->osh, D11_PSM_PHY_CTL_ALTBASE(regs, pi->regoffsets));
		if ((reg_val & ~inv_bit_mask) != desired_bit_val) {
			W_REG(pi->sh->osh, D11_PSM_PHY_CTL_ALTBASE(regs, pi->regoffsets),
				((reg_val & inv_bit_mask) | desired_bit_val));
		}

		/* Now restore maccontrol */
		W_REG(pi->sh->osh, D11_MACCONTROL_ALTBASE(regs, pi->regoffsets), mac_ctl);

		/* restore cur core */
		wlapi_si_d11_switch_addrbase(pi->sh->physhim, curr_coreidx);
	}
}
void wlc_phy_logen_reset(phy_info_t *pi, uint8 core)
{
	if (!(RADIOID_IS(pi->pubpi->radioid, BCM20693_ID) && RADIOMAJORREV(pi) == 3)) {
		MOD_PHYREGCE(pi, RfctrlCoreTxPus, core, logen_reset, 1);
		OSL_DELAY(1);
		MOD_PHYREGCE(pi, RfctrlCoreTxPus, core, logen_reset, 0);
	} else {
		if (CHSPEC_IS2G(pi->radio_chanspec)) {
			MOD_RADIO_PLLREG_20693(pi, LO2G_LOGEN0_CFG1, 0, logen0_reset, 1);
			MOD_RADIO_PLLREG_20693(pi, LO2G_LOGEN1_CFG1, 1, logen1_reset, 1);
			OSL_DELAY(1);
			MOD_RADIO_PLLREG_20693(pi, LO2G_LOGEN0_CFG1, 0, logen0_reset, 0);
			MOD_RADIO_PLLREG_20693(pi, LO2G_LOGEN1_CFG1, 1, logen1_reset, 0);
		} else {
			MOD_RADIO_PLLREG_20693(pi, LO5G_MISC1, 0, lo5g_reset, 1);
			OSL_DELAY(1);
			MOD_RADIO_PLLREG_20693(pi, LO5G_MISC1, 0, lo5g_reset, 0);
		}
	}
}
