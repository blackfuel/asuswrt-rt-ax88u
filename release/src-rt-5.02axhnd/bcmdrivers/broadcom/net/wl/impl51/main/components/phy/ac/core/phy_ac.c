static void
BCMATTACHFN(phy_ac_nvram_attach)(phy_ac_info_t *aci)
{
	phy_info_t *pi = aci->pi;
	/* read in temperature at calibration time */
	int16 tmp2 = (int16) (((int16)PHY_GETINTVAR(pi, rstr_rawtempsense))  << 7) >> 7;
	if (tmp2 == -1) {
		/* set to some bogus value, since nothing was written to SROM */
		pi->srom_rawtempsense = 255;
	} else {
		pi->srom_rawtempsense = tmp2;
	}
	/*
	* Fix me!
	* These epaxxx and extxxx are used by ipa2g_on and ipa5g_on during attach,
	* which are used by wlc_phy_set_txgain_tbls function and chanmgr module
	* during attach. Now, wlc_phy_set_txgain_tbls has not been moved to tbl module.
	* Once it would been moved, these epaxxx parameters' initialization and function
	* call of phy_ac_tpc_ipa_upd should be moved to module tbl or chanmgr,
	* depending on which comes first during attach
	*/
	pi->epagain2g = (uint8)PHY_GETINTVAR_DEFAULT_SLICE(pi, rstr_epagain2g, 0);
	pi->epagain5g = (uint8)PHY_GETINTVAR_DEFAULT_SLICE(pi, rstr_epagain5g, 0);
	pi->extpagain2g = (uint8)PHY_GETINTVAR_DEFAULT_SLICE(pi, rstr_extpagain2g, 0);
	pi->extpagain5g = (uint8)PHY_GETINTVAR_DEFAULT_SLICE(pi, rstr_extpagain5g, 0);
}

